import { tool } from "@opencode-ai/plugin"
import { lstat, readFile } from "node:fs/promises"
import path from "node:path"

function resolveInsideWorkspace(contextDir: string, input: string) {
  const base = path.resolve(contextDir)
  const target = path.resolve(base, input)
  const rel = path.relative(base, target)
  if (rel.startsWith("..") || path.isAbsolute(rel)) throw new Error(`file must stay inside the current workspace: ${input}`)
  return target
}

export default tool({
  description: "CTF artifact pager: structured paging/search for long artifact files generated by triage/decompile tools.",
  args: {
    file: tool.schema.string().describe("Workspace-relative artifact file"),
    query: tool.schema.string().optional().describe("Optional regex query; if present returns matching lines with context."),
    offset: tool.schema.number().optional().describe("0-based line offset for paging. Default 0."),
    limit: tool.schema.number().optional().describe("Maximum lines to return. Default 120."),
    context: tool.schema.number().optional().describe("Context lines around query hits. Default 2."),
    jsonOnly: tool.schema.boolean().optional().describe("Return JSON only. Default false."),
  },
  async execute(args, context) {
    const file = resolveInsideWorkspace(context.directory, args.file)
    const st = await lstat(file)
    if (!st.isFile()) throw new Error("file must be a regular file")
    const text = await readFile(file, "utf8")
    const lines = text.split(/\r?\n/)
    const limit = Math.max(10, Math.min(args.limit ?? 120, 500))
    const offset = Math.max(0, args.offset ?? 0)
    const ctx = Math.max(0, Math.min(args.context ?? 2, 20))
    let rows: Array<{ line: number; text: string }> = []
    if (args.query) {
      const re = new RegExp(args.query, "i")
      const seen = new Set<number>()
      for (let i = 0; i < lines.length; i++) {
        if (!re.test(lines[i])) continue
        for (let j = Math.max(0, i - ctx); j <= Math.min(lines.length - 1, i + ctx); j++) seen.add(j)
        if (seen.size >= limit + offset) break
      }
      rows = [...seen].sort((a, b) => a - b).slice(offset, offset + limit).map((i) => ({ line: i + 1, text: lines[i] }))
    } else {
      rows = lines.slice(offset, offset + limit).map((x, i) => ({ line: offset + i + 1, text: x }))
    }
    const payload = { file, size: st.size, totalLines: lines.length, offset, limit, query: args.query || "", rows, nextOffset: offset + rows.length < lines.length ? offset + rows.length : null }
    if (args.jsonOnly) return JSON.stringify(payload, null, 2)
    return [
      "verdict: artifact_page",
      `file: ${file}`,
      `size: ${st.size}`,
      `totalLines: ${lines.length}`,
      `offset: ${offset}`,
      `limit: ${limit}`,
      `query: ${args.query || "none"}`,
      "content:",
      ...rows.map((r) => `${r.line}: ${r.text}`),
      `nextOffset: ${payload.nextOffset ?? "none"}`,
    ].join("\n")
  },
})
